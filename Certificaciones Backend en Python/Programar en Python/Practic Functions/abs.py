# abs (x)  retorna el valor absoluto de un número. El argumento puede ser un entero, un decimal, o un objeto implementando __abs__(). Si el argumento es un número complejo, su magnitud es retornada.
         # Si el número que recibe no es negativo devuelve el mismo valor sin cambios.
print(abs(-15.5)) # 15.5
print(abs(-0.01)) # 0.01
print(abs(-7)) # 7
print(abs(-32)) # 32

